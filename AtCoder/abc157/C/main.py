#!/usr/bin/env python3
import sys


def solve(N: int, M: int, s: "List[int]", c: "List[int]"):
    digimap = {}

    for si, ci in zip(s, c):
        if not si in digimap:
            digimap[si] = ci
        else:
            if ci != digimap[si]:
                print(-1)
                return

    if 1 in digimap and digimap[1] == 0:
        if N == 1:
            print(0)
            return
        else:
            print(-1)
            return

    if len(digimap.keys()) == 0:
        if N == 1:
            print(0)
            return
        elif N == 2:
            print(10)
            return
        elif N == 3:
            print(100)
            return

    res = []
    for i in range(1, N + 1):
        if i in digimap:
            res.append(digimap[i])
        else:
            if i == 1:
                res.append(1)
            else:
                res.append(0)

    print("".join([str(x) for x in res]))
    return


